#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	a;
	char		c;

	a = n;
	if (a < 0)
	{
		a = -a;
		write(fd, "-", 1);
	}
	if (a > 9)
	{
		ft_putnbr_fd(a / 10, fd);
	}
	c = a % 10 + 48;
	write(fd, &c, 1);
}
